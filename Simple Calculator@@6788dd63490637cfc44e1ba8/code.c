#include<stdio.h>
int main () {
    int x,y;
    scanf("%d",&x);
    scanf("%d",&y);
    int c;
    if (x+y=c) {
        printf("%d",c);
    }
    else if (x/y=c) {
        printf("%d",c);
    }
    else if (x-y=c) {
        printf("%d",c);
    }
    else if (x*y=c) {
        printf("%d",c);
    }
}